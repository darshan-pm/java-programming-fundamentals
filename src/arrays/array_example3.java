public class array_example3 {
    public static void main(String[] args) {
        int num[] = new int[5];
        num[0] = 9;
        num[1] = 8;
        num[2] = 7;
        num[3] = 6;
        num[4] = 5;

        for (int i = 0; i <= 4; i++) {
            System.out.println(num[i]);
        }
    }
}
